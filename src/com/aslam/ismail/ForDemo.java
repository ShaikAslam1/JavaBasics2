package com.aslam.ismail;

public class ForDemo {
    public static void main(String[] args) {
        for (int i=1; i<11;i++){
            System.out.println(i+ " "); //increment//
        }
        int a = 200;
        System.out.println(a);
        for (int i =10;i>1;i--){
            System.out.println(i+ " "); //decrement//
        }
    }
}